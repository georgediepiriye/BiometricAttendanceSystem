package entity;

public class Lecturer {
	private Integer lecturerId;
	private String lecturerTitle;
	private String lecturerFirstName;
	private String lecturerLastName;
	private String lecturerCourseCode;
	private String lecturerCourseTitle;
	private String lecturerEmail;
	private String lecturerPassword;
	
	public String getLecturerPassword() {
		return lecturerPassword;
	}
	public void setLecturerPassword(String lecturerPassword) {
		this.lecturerPassword = lecturerPassword;
	}

	
	public String getLecturerEmail() {
		return lecturerEmail;
	}
	public void setLecturerEmail(String lecturerEmail) {
		this.lecturerEmail = lecturerEmail;
	}
	public Integer getLecturerId() {
		return lecturerId;
	}
	public void setLecturerId(Integer lecturerId) {
		this.lecturerId = lecturerId;
	}
	public String getLecturerTitle() {
		return lecturerTitle;
	}
	public void setLecturerTitle(String lecturerTitle) {
		this.lecturerTitle = lecturerTitle;
	}
	public String getLecturerFirstName() {
		return lecturerFirstName;
	}
	public void setLecturerFirstName(String lecturerFirstName) {
		this.lecturerFirstName = lecturerFirstName;
	}
	public String getLecturerLastName() {
		return lecturerLastName;
	}
	public void setLecturerLastName(String lecturerLastName) {
		this.lecturerLastName = lecturerLastName;
	}
	public String getLecturerCourseCode() {
		return lecturerCourseCode;
	}
	public void setLecturerCourseCode(String lecturerCourseCode) {
		this.lecturerCourseCode = lecturerCourseCode;
	}
	public String getLecturerCourseTitle() {
		return lecturerCourseTitle;
	}
	public void setLecturerCourseTitle(String lecturerCourseTitle) {
		this.lecturerCourseTitle = lecturerCourseTitle;
	}


}
